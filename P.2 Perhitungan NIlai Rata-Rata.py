# Perhitungan nilai 6 besar

Bindo = int(input("Nilai Bahasa Indonesia = "))
Bing = int(input("Nilai Bahasa Inggris   = "))
Mtk = int(input("Nilai Matematika       = "))
Fsk = int(input("Nilai Fisika           = "))
Bio = int(input("Nilai Biologi          = "))
Kimia = int(input("Nilai Kimia            = "))

hasil = (Bindo + Bing + Mtk + Fsk + Bio + Kimia) / 6

print(f"Rata-Rata Nilai Kamu adalah {hasil:.2f}")
